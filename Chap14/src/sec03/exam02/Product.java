package sec03.exam02;

public class Product {

	private int pno;
	private String name;
	private int price;
	private int stock;
	// 아 맞다..getter setter 생성 기능 있었지?
	// Source - Generate Getters and Setters...

	public int getPno() {
		return pno;
	}

	public void setPno(int pno) {
		this.pno = pno;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getStock() {
		return stock;
	}

	public void setStock(int stock) {
		this.stock = stock;
	}

}
